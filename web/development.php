<a href="http://sourceforge.net/mail/?group_id=32783">mailing lists</a>, a
<a href="http://sourceforge.net/pm/?group_id=32783">task manager</a>, and a web-based
<a href="http://cvs.sourceforge.net/cgi-bin/viewcvs.cgi/audiere">CVS viewer</a>.
</p>

<p>
Audiere is currently pretty decent at streaming audio.  However, it is not so
great at sound effects...  The next minor version (1.1.0 or 1.2.0) will
feature a completely new API, non-streaming audio, and more flexible audio
stream sources.
</p>

<p>
Help is wanted.  If you are interested in contributing, check out the feature
requests, bug reports, and task manager, and let
<a href="mailto:audiere-devel@lists.sf.net">audiere-devel</a> know what
you want to work on.
</p>

